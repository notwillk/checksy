#!/usr/bin/env bash
set -euo pipefail

: "${CHECKSY_PROVISION_TRACE:?}"
: "${CHECKSY_PROVISION_FIXED:?}"
: "${CHECKSY_PROVISION_READY_FIFO:?}"
: "${CHECKSY_PROVISION_RELEASE_FIFO:?}"

printf 'ready\n' > "$CHECKSY_PROVISION_READY_FIFO"
IFS= read -r _ < "$CHECKSY_PROVISION_RELEASE_FIFO"
printf 'fix\n' >> "$CHECKSY_PROVISION_TRACE"
: > "$CHECKSY_PROVISION_FIXED"
